# crie um programa que leia as 2 notas de um aluno
# calcule e mostre a média
nota1 = int(input("Por favor, insira sua primeira nota: "))
nota2 = int(input("Insira sua segunda nota: "))
print("Suas notas são {} e {} ".format(nota1, nota2))
media = (nota1 + nota2) / 2
print("Sua média é {}".format(media))
